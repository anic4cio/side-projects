let dez = 10
console.log(dez++)  // 10
console.log(dez)    // 11
console.log(dez++)  // 11
console.log(dez)    // 12

/* 
Importante se atentar que a ordem do operador altera no print
do console.log, colocando o operador ++/-- com prefixo ou sufixo
*/

let vinte = 20
console.log(--vinte)  // 19
console.log(vinte)    // 19
console.log(--vinte)  // 18
console.log(vinte)    // 18

