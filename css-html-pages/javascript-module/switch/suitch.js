let velocity = 80

switch(velocity) {
    case 80:
        console.log('Almost 80 velocity')
        break
    case 90:
        console.log('Almost 90 velocity')
        break
    case 100:
        console.log('Normal 100 velocity')
        break
    case 110:
        console.log('High velocity')
        break
    default:
        console.log('Default')

}